void drawchar_transparent(unsigned char c, int x, int y, int fgcolor)
{
	int cx,cy;
	int mask[8]={1,2,4,8,16,32,64,128};
	unsigned char *glyph=font+(int)c*16;
 
	for(cy=0;cy<16;cy++){
		for(cx=0;cx<8;cx++){
			if(glyph[cy]&mask[cx]) putpixelc(fgcolor,x+cx,y+cy-12);
		}
	}
}
void clear_screen_a(){
    for (int i = 0; i < 640*480; i++){
        drawchar_transparent(' ', i%640, i/640, FB_BLACK);
    }
}

void fb_write_fontbyte(unsigned char b)
{
    drawchar_transparent(b, cursor_pos_a%640, cursor_pos_a/640, FB_LIGHT_GREY);
    if (cursor_pos_a > FB_CELLS) {
        clear_screen_a();
        cursor_pos_a = 0;
    }
    cursor_pos_a++;
}
